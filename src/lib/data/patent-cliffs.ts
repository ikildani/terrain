// Sources: FDA Orange Book, USPTO patent databases, SEC filings,
// EvaluatePharma, Cortellis, company 10-K/20-F annual reports,
// published analyst consensus estimates.
//
// Revenue figures: peak and current revenue from company annual
// reports and consensus Wall Street estimates (2024-2025 data).
// ============================================================

export interface PatentCliffRecord {
  drug_name: string; // Brand name
  generic_name: string; // INN
  company: string;
  indication: string; // Must match indication-map.ts names
  therapy_area: string; // Must match the 18 canonical therapy areas
  us_loe_year: number; // Year of US loss of exclusivity
  eu_loe_year: number | null; // Year of EU LOE (null if unknown)
  peak_revenue_usd_b: number; // Peak annual revenue in $B
  current_revenue_usd_b: number; // Most recent annual revenue in $B
  exclusivity_type: 'patent' | 'orphan' | 'biologic' | 'pediatric_extension' | 'nce';
  biosimilar_competition: boolean; // Are biosimilars approved or expected?
  generic_competition: boolean; // Are generics approved or expected?
  market_impact: 'high' | 'medium' | 'low'; // How much will LOE disrupt the market?
  source: string;
}

