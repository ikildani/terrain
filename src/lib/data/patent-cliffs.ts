/**
 * Returns all patent cliff records matching a given indication name.
 * Uses bidirectional substring matching (case-insensitive) consistent
 * with the pattern used across indication-subtypes, biomarker-prevalence,
 * and other data lookup functions in the codebase.
 */
export function getPatentCliffsForIndication(indication: string): PatentCliffRecord[] {
  const query = indication.toLowerCase();
  return PATENT_CLIFF_DATA.filter((record) => {
    const recordInd = record.indication.toLowerCase();
    return recordInd.includes(query) || query.includes(recordInd);
  });
}

/**
 * Returns all patent cliff records for a given therapy area.
 */
export function getPatentCliffsForTherapyArea(therapyArea: string): PatentCliffRecord[] {
  const lower = therapyArea.toLowerCase();
  return PATENT_CLIFF_DATA.filter((record) => record.therapy_area.toLowerCase() === lower);
}

// ────────────────────────────────────────────────────────────
// LOE IMPACT SCORING
// ────────────────────────────────────────────────────────────

/**
 * Computes a 0-1 LOE impact score for an indication. Higher scores
 * indicate more LOE-driven opportunity: leaders are losing exclusivity
 * soon, large revenue pools are at risk, and the competitive landscape
 * is about to open up.
 *
 * Components:
 *   1. Temporal proximity (50% weight): How close are upcoming cliffs?
 *      Cliffs within 3 years score highest; past cliffs still count
 *      but at reduced weight.
 *   2. Revenue at risk (30% weight): Total revenue exposed to LOE,
 *      normalized against a $20B reference ceiling.
 *   3. Market impact breadth (20% weight): Proportion of records
 *      flagged as high or medium market impact.
 *
 * The current year is determined dynamically.
 */
export function computeLoeImpactScore(indication: string): {
  score: number; // 0-1, higher = more LOE opportunity
  nearestCliffYear: number | null;
  totalRevenueAtRisk: number;
  cliffCount: number;
} {
  const cliffs = getPatentCliffsForIndication(indication);

  if (cliffs.length === 0) {
    return { score: 0, nearestCliffYear: null, totalRevenueAtRisk: 0, cliffCount: 0 };
  }

  const currentYear = new Date().getFullYear();
  const HORIZON_YEARS = 8; // Look forward 8 years for upcoming cliffs
  const LOOKBACK_YEARS = 3; // Also consider recent past cliffs (biosimilar/generic ramp)

  // Deduplicate by drug_name to avoid double-counting revenue for drugs
  // with multiple indication entries in the same query result
  const seenDrugs = new Set<string>();
  let totalRevenueAtRisk = 0;

  // Find the nearest future cliff year (or most recent past cliff)
  let nearestCliffYear: number | null = null;
  let nearestDelta = Infinity;

  for (const cliff of cliffs) {
    const delta = cliff.us_loe_year - currentYear;
    if (Math.abs(delta) < Math.abs(nearestDelta)) {
      nearestDelta = delta;
      nearestCliffYear = cliff.us_loe_year;
    }
    // Accumulate revenue — deduplicated by drug
    if (!seenDrugs.has(cliff.drug_name)) {
      seenDrugs.add(cliff.drug_name);
      // For past LOE, use current revenue (still being eroded);
      // for future LOE, use peak revenue (revenue at risk)
      if (delta <= 0) {
        totalRevenueAtRisk += cliff.current_revenue_usd_b;
      } else {
        totalRevenueAtRisk += cliff.peak_revenue_usd_b;
      }
    }
  }

  // 1. Temporal proximity score (0-1)
  // Weight each cliff by how close it is. Nearest cliffs = highest weight.
  let temporalSum = 0;
  let temporalMax = 0;
  for (const cliff of cliffs) {
    const yearsToCliff = cliff.us_loe_year - currentYear;
    let proximityWeight: number;
    if (yearsToCliff < -LOOKBACK_YEARS) {
      // Too far in the past — already fully eroded
      proximityWeight = 0;
    } else if (yearsToCliff <= 0) {
      // Recent past cliff — still relevant (biosimilar ramp takes 3-5 years)
      proximityWeight = 0.5 + (yearsToCliff + LOOKBACK_YEARS) / (LOOKBACK_YEARS * 2);
    } else if (yearsToCliff <= 3) {
      // Imminent cliff — highest score
      proximityWeight = 1.0 - yearsToCliff * 0.05;
    } else if (yearsToCliff <= HORIZON_YEARS) {
      // Medium-term cliff
      proximityWeight = 0.7 - (yearsToCliff - 3) * 0.1;
    } else {
      // Far future — minimal score
      proximityWeight = Math.max(0, 0.2 - (yearsToCliff - HORIZON_YEARS) * 0.05);
    }
    const impactWeight = cliff.market_impact === 'high' ? 1.0 : cliff.market_impact === 'medium' ? 0.6 : 0.3;
    temporalSum += proximityWeight * impactWeight;
    temporalMax += impactWeight;
  }
  const temporalScore = temporalMax > 0 ? Math.min(temporalSum / temporalMax, 1) : 0;

  // 2. Revenue at risk score (0-1)
  // Normalize against $20B reference ceiling
  const REVENUE_CEILING = 20;
  const revenueScore = Math.min(totalRevenueAtRisk / REVENUE_CEILING, 1);

  // 3. Market impact breadth (0-1)
  const highMediumCount = cliffs.filter((c) => c.market_impact === 'high' || c.market_impact === 'medium').length;
  const impactBreadth = Math.min(highMediumCount / Math.max(cliffs.length, 1), 1);

  // Weighted composite
  const score = Math.round((temporalScore * 0.5 + revenueScore * 0.3 + impactBreadth * 0.2) * 100) / 100;

  return {
    score: Math.min(score, 1),
    nearestCliffYear,
    totalRevenueAtRisk: Math.round(totalRevenueAtRisk * 10) / 10,
    cliffCount: cliffs.length,
  };
}
