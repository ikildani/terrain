/**
 * Find all nutraceutical partners of a specific type
 * (e.g., 'contract_manufacturer', 'ingredient_supplier', 'strategic_acquirer')
 */
export function findNutraPartnersByType(
  type: NutraceuticalPartnerType
): NutraceuticalPartnerProfile[] {
  return NUTRACEUTICAL_PARTNER_DATABASE.filter(
    (p) => p.partner_type === type
  );
}

/**
 * Find all nutraceutical partners that serve a specific channel
 * (e.g., 'dtc_ecommerce', 'amazon', 'retail_mass', 'practitioner')
 */
export function findNutraPartnersByChannel(
  channel: NutraceuticalChannel
): NutraceuticalPartnerProfile[] {
  return NUTRACEUTICAL_PARTNER_DATABASE.filter(
    (p) => p.channels_served.includes(channel)
  );
}

/**
 * Find all nutraceutical partners that serve a specific product category
 * (e.g., 'longevity_compound', 'dietary_supplement', 'probiotic_microbiome')
 */
export function findNutraPartnersByCategory(
  category: NutraceuticalCategory
): NutraceuticalPartnerProfile[] {
  return NUTRACEUTICAL_PARTNER_DATABASE.filter(
    (p) => p.categories_served.includes(category)
  );
}
