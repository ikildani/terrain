/** Filter precedent trials by therapy area */
export function getTrialsByTherapyArea(area: string): PrecedentTrialRecord[] {
  return PRECEDENT_TRIALS.filter(
    (t) => t.therapy_area.toLowerCase() === area.toLowerCase()
  );
}

/** Filter precedent trials by indication keyword */
export function getTrialsByIndication(keyword: string): PrecedentTrialRecord[] {
  const lower = keyword.toLowerCase();
  return PRECEDENT_TRIALS.filter(
    (t) =>
      t.indication.toLowerCase().includes(lower) ||
      t.drug.toLowerCase().includes(lower)
  );
}

/** Get only approved trials (excludes failed/discontinued) */
export function getApprovedTrials(): PrecedentTrialRecord[] {
  return PRECEDENT_TRIALS.filter((t) => t.approval_year > 0);
}

/** Get accelerated approval precedents */
export function getAcceleratedApprovalTrials(): PrecedentTrialRecord[] {
  return PRECEDENT_TRIALS.filter((t) => t.was_accelerated);
}

/** Get biomarker-enriched trial designs */
export function getBiomarkerEnrichedTrials(): PrecedentTrialRecord[] {
  return PRECEDENT_TRIALS.filter((t) => t.biomarker_enriched);
}

/** Get adaptive design trials */
export function getAdaptiveDesignTrials(): PrecedentTrialRecord[] {
  return PRECEDENT_TRIALS.filter((t) => t.adaptive_design);
}

/** Find trials with similar sample size range (within +/- 30%) */
export function getTrialsBySampleSizeRange(
  targetSize: number
): PrecedentTrialRecord[] {
  const low = targetSize * 0.7;
  const high = targetSize * 1.3;
  return PRECEDENT_TRIALS.filter(
    (t) => t.sample_size >= low && t.sample_size <= high
  );
}
