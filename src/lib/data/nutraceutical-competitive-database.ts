export const NUTRACEUTICAL_BRAND_DATABASE: NutraceuticalBrandEntry[] = [
  ...LONGEVITY_BRANDS,
  ...COGNITIVE_BRANDS,
  ...GUT_BRANDS,
  ...METABOLIC_BRANDS,
  ...IMMUNE_BRANDS,
  ...SLEEP_BRANDS,
  ...SPORTS_BRANDS,
  ...COLLAGEN_BRANDS,
  ...MULTIVITAMIN_BRANDS,
  ...ANTI_INFLAMMATORY_BRANDS,
  ...HORMONE_BRANDS,
  ...JOINT_BRANDS,
];

// =============================================================================
// FINDER FUNCTIONS
// =============================================================================

/**
 * Fuzzy match brands by ingredient name against primary_ingredients.
 * Case-insensitive partial matching  e.g. "nmn" matches "nicotinamide mononucleotide (NMN)".
 */
export function findCompetitorsByIngredient(ingredient: string): NutraceuticalBrandEntry[] {
  const needle = ingredient.toLowerCase().trim();
  return NUTRACEUTICAL_BRAND_DATABASE.filter((entry) =>
    entry.primary_ingredients.some((ing) => ing.toLowerCase().includes(needle))
  );
}

/**
 * Exact match on ingredient_category.
 * Accepts the raw category slug (e.g. 'longevity', 'gut_microbiome').
 */
export function findCompetitorsByCategory(category: string): NutraceuticalBrandEntry[] {
  const normalized = category.toLowerCase().trim();
  return NUTRACEUTICAL_BRAND_DATABASE.filter(
    (entry) => entry.ingredient_category === normalized
  );
}

/**
 * Return top brands sorted by estimated annual revenue (descending).
 * Optionally filter by category. Defaults to returning all brands if no limit.
 */
export function getTopBrandsByRevenue(
  category?: string,
  limit?: number
): NutraceuticalBrandEntry[] {
  let results = category
    ? findCompetitorsByCategory(category)
    : [...NUTRACEUTICAL_BRAND_DATABASE];

  results.sort((a, b) => b.estimated_annual_revenue_m - a.estimated_annual_revenue_m);

  if (limit && limit > 0) {
    results = results.slice(0, limit);
  }

  return results;
}
