// ────────────────────────────────────────────────────────────
// Pre-built indexes for O(1) lookups on the 200+ deal dataset
// ────────────────────────────────────────────────────────────

const _indexByTA: Map<string, PharmaDealComp[]> = new Map();
const _indexByStage: Map<string, PharmaDealComp[]> = new Map();

function _ensureIndexes() {
  if (_indexByTA.size > 0) return;
  for (const deal of PHARMA_DEAL_COMPS) {
    const ta = deal.therapy_area.toLowerCase();
    if (!_indexByTA.has(ta)) _indexByTA.set(ta, []);
    _indexByTA.get(ta)!.push(deal);

    const st = deal.development_stage.toLowerCase();
    if (!_indexByStage.has(st)) _indexByStage.set(st, []);
    _indexByStage.get(st)!.push(deal);
  }
}

// ────────────────────────────────────────────────────────────
// HELPER: Filter comps by therapy area and/or development stage
// Uses indexed maps for efficient filtering on 200+ records.
