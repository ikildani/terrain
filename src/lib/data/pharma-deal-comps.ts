// ────────────────────────────────────────────────────────────
// HELPER: Filter comps by therapy area and/or development stage
// ────────────────────────────────────────────────────────────

const STAGE_MATCH_MAP: Record<string, string[]> = {
  preclinical: ['Preclinical'],
  phase1: ['Phase 1', 'Preclinical'],
  phase2: ['Phase 2', 'Phase 1'],
  phase3: ['Phase 3', 'Phase 2'],
  approved: ['Approved', 'Phase 3'],
};

export function filterDealComps(therapyArea?: string, developmentStage?: string): PharmaDealComp[] {
  let filtered = [...PHARMA_DEAL_COMPS];

  // Filter by therapy area (exact match on normalized name)
  if (therapyArea) {
    const taLower = therapyArea.toLowerCase();
    const taMatches = filtered.filter((d) => d.therapy_area.toLowerCase() === taLower);
    // If we have at least 3 TA matches, use them; otherwise broaden
    if (taMatches.length >= 3) {
      filtered = taMatches;
    }
  }

  // Filter by development stage (allow adjacent stages for more comps)
  if (developmentStage) {
    const matchStages = STAGE_MATCH_MAP[developmentStage] ?? [developmentStage];
    const stageMatches = filtered.filter((d) =>
      matchStages.some((s) => d.development_stage.toLowerCase() === s.toLowerCase()),
    );
    // If we have at least 3 stage matches within TA, use them
    if (stageMatches.length >= 3) {
      filtered = stageMatches;
    }
  }

  // Sort by year descending (most recent first)
  filtered.sort((a, b) => b.year - a.year);

  return filtered;
}
