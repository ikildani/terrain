export interface DealCompFilterOptions {
  therapyArea?: string;
  developmentStage?: string;
  dealType?: PharmaDealComp['deal_type'];
  yearMin?: number;
  yearMax?: number;
  minDealValue?: number;
  maxDealValue?: number;
  limit?: number;
}

export function filterDealComps(
  therapyAreaOrOpts?: string | DealCompFilterOptions,
  developmentStage?: string,
): PharmaDealComp[] {
  _ensureIndexes();

  // Support both legacy (two-arg) and new (options object) signatures
  let opts: DealCompFilterOptions;
  if (typeof therapyAreaOrOpts === 'string' || therapyAreaOrOpts === undefined) {
    opts = { therapyArea: therapyAreaOrOpts as string | undefined, developmentStage };
  } else {
    opts = therapyAreaOrOpts;
  }

  let filtered: PharmaDealComp[] = [...PHARMA_DEAL_COMPS];

  // Filter by therapy area (exact match on normalized name, with alias support)
  if (opts.therapyArea) {
    let taLower = opts.therapyArea.toLowerCase().trim();
    taLower = TA_ALIAS_MAP[taLower] ?? taLower;
    const taMatches = _indexByTA.get(taLower) ?? [];
    // If we have at least 3 TA matches, use them; otherwise broaden
    if (taMatches.length >= 3) {
      filtered = [...taMatches];
    }
  }

  // Filter by development stage (allow adjacent stages for more comps)
  if (opts.developmentStage) {
    const stageKey = opts.developmentStage.toLowerCase().trim();
    const matchStages = STAGE_MATCH_MAP[stageKey] ?? [opts.developmentStage];
    const stageMatches = filtered.filter((d) =>
      matchStages.some((s) => d.development_stage.toLowerCase() === s.toLowerCase()),
    );
    if (stageMatches.length >= 3) {
      filtered = stageMatches;
    }
  }

  // Filter by deal type
  if (opts.dealType) {
    const dtMatches = filtered.filter((d) => d.deal_type === opts.dealType);
    if (dtMatches.length >= 2) {
      filtered = dtMatches;
    }
  }

  // Filter by year range
  if (opts.yearMin) {
    filtered = filtered.filter((d) => d.year >= opts.yearMin!);
  }
  if (opts.yearMax) {
    filtered = filtered.filter((d) => d.year <= opts.yearMax!);
  }

  // Filter by deal value range
  if (opts.minDealValue) {
    filtered = filtered.filter((d) => d.total_deal_value_m >= opts.minDealValue!);
  }
  if (opts.maxDealValue) {
    filtered = filtered.filter((d) => d.total_deal_value_m <= opts.maxDealValue!);
  }

  // Sort by year descending (most recent first)
  filtered.sort((a, b) => b.year - a.year || b.total_deal_value_m - a.total_deal_value_m);

  // Apply limit
  if (opts.limit && opts.limit > 0) {
    filtered = filtered.slice(0, opts.limit);
  }

  return filtered;
}

// ────────────────────────────────────────────────────────────
// HELPER: Get summary statistics for a filtered set of comps
// ────────────────────────────────────────────────────────────

export interface DealCompStats {
  count: number;
  medianMultiple: number;
  meanMultiple: number;
  medianUpfront: number;
  medianTotalValue: number;
  percentLicensing: number;
  yearRange: [number, number];
  /** Coverage breakdown by therapy area (TA name → deal count) */
  coverageByTA?: Record<string, number>;
  /** Coverage breakdown by year (year → deal count) */
  coverageByYear?: Record<number, number>;
  /** Coverage breakdown by development stage */
  coverageByStage?: Record<string, number>;
  /** Coverage breakdown by deal size bucket */
  coverageBySizeBucket?: Record<string, number>;
}

export function getDealCompStats(deals: PharmaDealComp[], options?: { includeCoverage?: boolean }): DealCompStats {
  if (deals.length === 0) {
    return {
      count: 0,
      medianMultiple: 0,
      meanMultiple: 0,
      medianUpfront: 0,
      medianTotalValue: 0,
      percentLicensing: 0,
      yearRange: [0, 0],
    };
  }

  const multiples = deals.map((d) => d.ev_peak_sales_multiple).sort((a, b) => a - b);
  const upfronts = deals.map((d) => d.upfront_m).sort((a, b) => a - b);
  const totals = deals.map((d) => d.total_deal_value_m).sort((a, b) => a - b);
  const years = deals.map((d) => d.year);

  const median = (arr: number[]) => {
    const mid = Math.floor(arr.length / 2);
    return arr.length % 2 !== 0 ? arr[mid] : (arr[mid - 1] + arr[mid]) / 2;
  };

  const result: DealCompStats = {
    count: deals.length,
    medianMultiple: Math.round(median(multiples) * 100) / 100,
    meanMultiple: Math.round((multiples.reduce((a, b) => a + b, 0) / multiples.length) * 100) / 100,
    medianUpfront: median(upfronts),
    medianTotalValue: median(totals),
    percentLicensing: Math.round((deals.filter((d) => d.deal_type === 'licensing').length / deals.length) * 100),
    yearRange: [Math.min(...years), Math.max(...years)],
  };

  if (options?.includeCoverage) {
    // Coverage by therapy area
    const byTA: Record<string, number> = {};
    for (const d of deals) {
      byTA[d.therapy_area] = (byTA[d.therapy_area] || 0) + 1;
    }
    result.coverageByTA = byTA;

    // Coverage by year
    const byYear: Record<number, number> = {};
    for (const d of deals) {
      byYear[d.year] = (byYear[d.year] || 0) + 1;
    }
    result.coverageByYear = byYear;

    // Coverage by development stage
    const byStage: Record<string, number> = {};
    for (const d of deals) {
      byStage[d.development_stage] = (byStage[d.development_stage] || 0) + 1;
    }
    result.coverageByStage = byStage;

    // Coverage by deal size bucket
    const bySize: Record<string, number> = {
      '<$50M': 0,
      '$50M-$200M': 0,
      '$200M-$1B': 0,
      '$1B-$5B': 0,
      '>$5B': 0,
    };
    for (const d of deals) {
      if (d.total_deal_value_m < 50) bySize['<$50M']++;
      else if (d.total_deal_value_m < 200) bySize['$50M-$200M']++;
      else if (d.total_deal_value_m < 1000) bySize['$200M-$1B']++;
      else if (d.total_deal_value_m < 5000) bySize['$1B-$5B']++;
      else bySize['>$5B']++;
    }
    result.coverageBySizeBucket = bySize;
  }

  return result;
}
