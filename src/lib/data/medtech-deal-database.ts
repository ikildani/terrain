/**
 * Filter deals by device category.
 */
export function getDealsByCategory(category: DeviceCategory): MedTechDeal[] {
  return MEDTECH_DEAL_DATABASE.filter((deal) => deal.device_category === category);
}

/**
 * Filter deals by deal type (acquisition, licensing, partnership, etc.).
 */
export function getDealsByType(type: MedTechDeal['deal_type']): MedTechDeal[] {
  return MEDTECH_DEAL_DATABASE.filter((deal) => deal.deal_type === type);
}

/**
 * Search deals involving a specific company (as acquirer or target).
 * Case-insensitive partial match.
 */
export function getDealsByCompany(company: string): MedTechDeal[] {
  const query = company.toLowerCase();
  return MEDTECH_DEAL_DATABASE.filter(
    (deal) =>
      deal.acquirer.toLowerCase().includes(query) ||
      deal.target.toLowerCase().includes(query),
  );
}

/**
 * Calculate the median deal value (in $M) for a given device category.
 * Only considers deals with a reported value. Returns 0 if no deals found.
 */
export function getMedianDealValue(category: DeviceCategory): number {
  const values = MEDTECH_DEAL_DATABASE
    .filter((deal) => deal.device_category === category && deal.value_m !== undefined)
    .map((deal) => deal.value_m as number)
    .sort((a, b) => a - b);

  if (values.length === 0) return 0;

  const mid = Math.floor(values.length / 2);
  return values.length % 2 !== 0
    ? values[mid]
    : (values[mid - 1] + values[mid]) / 2;
}

/**
 * Get deals announced within the last N years from today.
 */
export function getRecentDeals(years: number): MedTechDeal[] {
  const cutoff = new Date();
  cutoff.setFullYear(cutoff.getFullYear() - years);
  const cutoffISO = cutoff.toISOString().slice(0, 10);

  return MEDTECH_DEAL_DATABASE.filter((deal) => deal.announced_date >= cutoffISO);
}

/**
 * Aggregate deal volume and total disclosed value by calendar year.
 * Returns array sorted by year ascending.
 */
export function getDealVolumeByYear(): { year: number; count: number; total_value_m: number }[] {
  const yearMap = new Map<number, { count: number; total_value_m: number }>();

  for (const deal of MEDTECH_DEAL_DATABASE) {
    const year = parseInt(deal.announced_date.slice(0, 4), 10);
    const existing = yearMap.get(year) || { count: 0, total_value_m: 0 };
    existing.count += 1;
    if (deal.value_m !== undefined) {
      existing.total_value_m += deal.value_m;
    }
    yearMap.set(year, existing);
  }

  return Array.from(yearMap.entries())
    .map(([year, data]) => ({ year, ...data }))
    .sort((a, b) => a.year - b.year);
}
